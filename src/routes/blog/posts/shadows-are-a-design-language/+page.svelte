    <p>Shadows aren’t decoration—they communicate elevation and emphasis. Subtle changes in blur, spread, and opacity can make a UI feel premium or clunky.</p>
    <h2>Rules I follow</h2>
    <ul>
      <li>Fewer tiers, more consistency</li>
      <li>Lower opacity on dark themes (avoid heavy grays)</li>
      <li>Pair shadow with a thin border for crispness</li>
    </ul>
    <p>Good shadows disappear into the experience—bad ones demand attention.</p>
